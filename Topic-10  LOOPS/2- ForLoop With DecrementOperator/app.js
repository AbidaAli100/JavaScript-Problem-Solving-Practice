/*
        Create a for loop.

     1-Take the loop iterating variable as i.
     2-Initialize it with 200
     3-Print numbers from 200 till 0.
     4-Use the decrement operator as -=3.
*/

for (let i = 200; i >= 0; i -= 3) {
    console.log(i);
}
// the decrement stops at 2, because the increment operator is -3.
